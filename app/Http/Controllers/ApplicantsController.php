    {
        $validator = Validator::make($request->all(), [

            'id_pic' => 'required', 
            'birth_cert' => 'required', 
            'report_card' => 'required',

        ]);

        if ($validator->fails()) {
            return redirect()->route('admissionForm')
                         ->with('error', 'upload ulit')
                         ->with('active', 'upload_ulit');
        }
    }

